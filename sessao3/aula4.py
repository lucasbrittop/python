# tipo int e float
# int -> numero inteiro
# o tipo int representa qualquer numero
# positivo ou negativo. int sem sinal é considerado positivo
print(11) #int
print(-11) #int
print(0) #int

#float -> numero com ponto flutuante
# o tipo float representa qualuqer número
# positivo ou negativo com ponto flutuante
# float sem sinal é positivo
print(1.1) #float
print(0.0)#float
print(-1.5)#float

# a função type mostra o tipo que o python inferiu ao valor
print(type('lucas'))
print(type(1))
print(type(1.5))